package task.Annotations;

public enum PropertyType {
    STRING,      // Строковый тип
    INTEGER,     // Целое число
    LONG,        // Длинное целое
    DOUBLE,      // Число с плавающей точкой
    BOOLEAN,     // Логический тип
    STRING_ARRAY,// Массив строк
    LIST,        // Список
    SET,         // Множество
    AUTO         // Автоматическое определение
}